package com.bridge.enums;

/**
 *
 * @author Supriya
 */
public enum BuySell {

    NEUTRAL(0, "NEUTRAL"),
    BUY(1, "BUY"),
    SELL(2, "SELL");

    private int id;
    private String name;

    private BuySell(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
